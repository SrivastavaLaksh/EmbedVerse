def get_domain_words(domain: str) -> List[str]:
    domain_lower = domain.lower()
    if domain_lower in TECHNICAL_DOMAIN_WORDS:
        return TECHNICAL_DOMAIN_WORDS[domain_lower]
    raise ValueError(
        f"Unknown domain: {domain}. Available: {list(TECHNICAL_DOMAIN_WORDS.keys())}"
    )


def get_all_domains() -> List[str]:
    return list(TECHNICAL_DOMAIN_WORDS.keys())


def get_random_words(domain: str, n: int = 20) -> List[str]:
    words = get_domain_words(domain)
    import random

    random.shuffle(words)
    return words[: min(n, len(words))]


